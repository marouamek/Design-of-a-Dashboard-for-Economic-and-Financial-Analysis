-- Création de la base de données
CREATE DATABASE IF NOT EXISTS db_ipc;

USE db_ipc;

-- Table Dimension : Année
CREATE TABLE Dim_Annee (
    id_annee INT AUTO_INCREMENT PRIMARY KEY,
    annee INT NOT NULL
);

-- Table Dimension : Groupe
CREATE TABLE Dim_Groupe (
    id_groupe INT AUTO_INCREMENT PRIMARY KEY,
    label_grp VARCHAR(255) NOT NULL,
    poids_groupe DECIMAL(5, 2) DEFAULT 0
);

-- Table Dimension : Sous-Groupe
CREATE TABLE Dim_Sous_Groupe (
    id_sous_groupe INT AUTO_INCREMENT PRIMARY KEY,
    label_sous_grp VARCHAR(255) NOT NULL,
    id_groupe INT NOT NULL,
    poids_sous_groupe DECIMAL(5, 2) DEFAULT 0,
    FOREIGN KEY (id_groupe) REFERENCES Dim_Groupe (id_groupe)
);

-- Table de Faits : Fact_IPC
CREATE TABLE Fact_IPC (
    id_fact INT AUTO_INCREMENT PRIMARY KEY,
    id_annee INT NOT NULL,
    id_groupe INT NOT NULL,
    id_sous_groupe INT NOT NULL,
    ipc DECIMAL(10, 2),
    variation DECIMAL(5, 2),
    FOREIGN KEY (id_annee) REFERENCES Dim_Annee (id_annee),
    FOREIGN KEY (id_groupe) REFERENCES Dim_Groupe (id_groupe),
    FOREIGN KEY (id_sous_groupe) REFERENCES Dim_Sous_Groupe (id_sous_groupe)
);

CREATE TABLE evolution_globale (
    annee INT PRIMARY KEY,
    indice_general DECIMAL(10, 2),
    variation_pourcentage DECIMAL(5, 2)
);

CREATE TABLE Fact_Variation_Groupe (
    id_fact INT AUTO_INCREMENT PRIMARY KEY,
    annee INT NOT NULL,
    id_groupe INT NOT NULL,
    variation_annuelle DECIMAL(5, 2),
    FOREIGN KEY (id_groupe) REFERENCES Dim_Groupe (id_groupe)
);

CREATE TABLE IndiceMois2024 (
    Mois VARCHAR(10) PRIMARY KEY,
    IndiceGeneral DECIMAL(6, 2),
    VariationMensuelle DECIMAL(4, 2)
);

